package day05_Variables;

public class ConcateExample {
    public static void main(String[] args) {
        System.out.println(5+4);// int(5)+ int(4)=int(9)
        System.out.println(5+4 +
                "hello");//int(5)+int(4)=int(9)+string("hello")=string(9hello)
        System.out.println("hello"+5+4);//string ("hello")+int(5)=string(hello5)+int(4)=string(hello54)
    }
}
