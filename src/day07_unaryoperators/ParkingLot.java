package day07_unaryoperators;

public class ParkingLot {
    public static void main(String[] args) {

        int cars=10;

        System.out.println("2 cars drive in to parking lot");
        cars++;
        cars++;
        System.out.println(cars+ " Now");
        System.out.println("5 cars left");
        cars--;
        cars--;
        cars--;
        cars--;
        cars--;
        System.out.println(cars +" Now");

    }
}
