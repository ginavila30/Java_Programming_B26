package day21_loops;

public class PrintNumbers {
    public static void main(String[] args) {
        int num=1;
        while(num<=100){

            if(num%2==0){
                System.out.println(num);

            }
            num++;

        }
        int a=0;
        while(a++<=100){

            if(a%2==1){
                System.out.print(a+" ");
                ;
            }
    }
}}
